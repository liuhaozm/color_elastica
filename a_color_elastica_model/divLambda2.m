function [dLambdax,dLambday]=divLambda2(lambda1,lambda2)

lambda_1ex=expandf3(lambda1);
lambda_2ex=expandf3(lambda2);
dLambdax=((lambda_1ex(3:end,2:end-1)-lambda_1ex(1:end-2,2:end-1))/(2*h)+...
    (lambda_2ex(3:end,2:end-1)+lambda_2ex(2:end-1,2:end-1))/(2*h)-...
    (lambda_2ex(2:end-1,1:end-2)+lambda_2ex(3:end,1:end-2))/(2*h));

dLambday=((lambda_1ex(2:end-1,2:end-1)+lambda_1ex(2:end-1,3:end))/(2*h)-...
    (lambda_1ex(1:end-2,2:end-1)+lambda_1ex(1:end-2,3:end))/(2*h)+...
    (lambda_2ex(2:end-1,3:end)-lambda_2ex(2:end-1,1:end-2))/(2*h));